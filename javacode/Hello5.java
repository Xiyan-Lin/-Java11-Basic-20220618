public class Hello5 {

	public static void main(String[] args) {
		var r = 10; // 半徑
		var pi = 3.14; // 圓周率
		System.out.println(r);
		System.out.println(pi);
		var area = r * r * pi; // 圓面積
		System.out.println(area);
		var volume = 4/3.0 * pi * r * r * r; // 球體積
		System.out.println(volume);
	}

}