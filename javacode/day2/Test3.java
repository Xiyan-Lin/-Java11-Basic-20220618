package day2;

public class Test3 {
	public static void main(String[] args) {
		// float(32 bits), double(64 bits)
		// 浮點數預設的資料型別是 double
		// ex:直接在程式中撰寫 3.14 的字面值
		// 系統自動會以 double 來看待
		float f = 3.14F;
		double d = 3.14;
		System.out.println(f);
		System.out.println(d);
		var v = 3.14;
		System.out.println(v);
	}
}